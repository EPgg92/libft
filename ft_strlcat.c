#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t len_dst;
	size_t len_src;
	size_t i;

	len_dst = ft_strlen(dst);
	len_src = ft_strlen(src);
	if (size <= len_dst)
		return (len_src + size);
	i = len_dst;
	size--;
	while (*src && i < size)
		*(dst + i++) = *src++;
	*(dst + i) = '\0';
	return (len_dst + len_src);
}
