#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		len;

	len = ft_strlen(s) + 1;
	s += len;
	while (len--)
		if (*(--s) == (unsigned char)c)
			return((char *)s);
	return (NULL);
}
