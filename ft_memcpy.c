#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char *mem;

	mem = (unsigned char *)dst;
	while (n--)
		*mem++ = *(unsigned char *)src++;
	return (dst);
}
